package com.example.tranquangvinh_android43_buoi5;

public class Food_Cart {

    String name;
    String count;

    public Food_Cart(String name, String count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }
}
